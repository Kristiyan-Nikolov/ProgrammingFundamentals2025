//1. въвеждам цяло число
int number = int.Parse(Console.ReadLine());

//2. проверка дали числото е четно или нечетно
//четно (разделяме го на 2 и получаваме остатък 0) -> "even"
//нечетно (разделяме го на 2 и получаваме остатък различен 0) -> "odd"

if (number % 2 == 0)
{
    //четно
    Console.WriteLine("even");
}
else
{
    //нечетно
    Console.WriteLine("odd");
}

